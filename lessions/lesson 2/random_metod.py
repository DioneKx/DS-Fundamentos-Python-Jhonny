import random

arrayChoice = [20, 5, 10, 15, 24]

print('Método random: randrange(100):', random.randrange(100))
print('Método random: randrange(50, 100):', random.randrange(50, 100))
print('Método random: choice([20, 5, 10, 15, 24]):', random.choice(arrayChoice))
print('Método random: random():', random.random())
print('Método random: randint(30, 60):', random.randint(30, 60))